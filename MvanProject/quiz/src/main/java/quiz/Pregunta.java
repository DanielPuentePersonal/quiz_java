package quiz;

public class Pregunta {

    public String texto;
    public String respuestasCorrectasTexto;
    public String respuestasCorrectas;

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }

    public String getrespuestasCorrectasTexto() {
        return respuestasCorrectasTexto;
    }

    public void setrespuestasCorrectasTexto(String respuestasCorrectasTexto) {
        this.respuestasCorrectasTexto = respuestasCorrectasTexto;
    }

    public String getRespuestasCorrectas() {
        return respuestasCorrectas;
    }

    public void setRespuestasCorrectas(String respuestasCorrectas) {
        this.respuestasCorrectas = respuestasCorrectas;
    }
}
